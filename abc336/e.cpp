using ll = long long;

int digitSum(long long n)
{
    int sum = 0;
    while (n > 0)
    {
        sum += n % 10;
        n /= 10;
    }
    return sum;
}

int main()
{
    ll N;
    cin >> N;

    int ans = 0;
    for (ll i = 1; i <= N; ++i)
    {
        if (i % digitSum(i) == 0)
            ans++;
    }

    cout << ans << endl;

    return 0;
}
